package arraylist;

public class Dog {
    private boolean hasfur;
    private String name;
    private String breed;

    public Dog(boolean hasfur, String name, String breed) {
        this.hasfur = hasfur;
        this.name = name;
        this.breed = breed;
    }

    public boolean isHasfur() {
        return hasfur;
    }

    public void setHasfur(boolean hasfur) {
        this.hasfur = hasfur;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getBreed() {
        return breed;
    }

    public void setBreed(String breed) {
        this.breed = breed;
    }






}
